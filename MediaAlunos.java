public class MediaAlunos {
    public static void main(String[] args) {
        int turmaA = 15;
        int turmaB = 10;
        int turmaC = 20;
        int somatotal = turmaA + turmaB + turmaC;
        double media = (double) somatotal / 3;
        System.out.println("Total de alunos nas três turmas: " + somatotal);
        System.out.println("A média de alunos por turma é: " + media);
    }
    
}
